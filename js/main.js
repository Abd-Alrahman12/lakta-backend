function setLanguage(lang) {
  const dict = translations[lang] || translations.en;
  document.documentElement.lang = lang;
  document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';

  document.querySelectorAll('[data-i18n]').forEach((el) => {
    const key = el.getAttribute('data-i18n');
    const value = dict[key];
    if (!value) return;
    if (key === 'footerCopy') {
      const year = new Date().getFullYear();
      el.textContent = value.replace('{{year}}', year);
    } else {
      el.textContent = value;
    }
  });

  document.querySelectorAll('.lang-btn').forEach((btn) => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });

  localStorage.setItem('siteLang', lang);
}

function initLanguage() {
  const stored = localStorage.getItem('siteLang');
  const initial = stored === 'ar' || stored === 'en' ? stored : 'en';
  setLanguage(initial);

  document.querySelectorAll('.lang-btn').forEach((btn) => {
    btn.addEventListener('click', () => {
      const lang = btn.dataset.lang;
      setLanguage(lang);
    });
  });
}

function initSmoothScroll() {
  document.body.addEventListener('click', (e) => {
    const target = e.target.closest('[data-scroll-target]');
    if (!target) return;
    const selector = target.getAttribute('data-scroll-target');
    const el = document.querySelector(selector);
    if (!el) return;
    e.preventDefault();
    el.scrollIntoView({ behavior: 'smooth', block: 'start' });
  });
}

function initNavToggle() {
  const header = document.querySelector('.site-header');
  const toggle = document.querySelector('.nav-toggle');
  if (!toggle) return;
  toggle.addEventListener('click', () => {
    header.classList.toggle('nav-open');
  });
  document.querySelectorAll('.main-nav a').forEach((link) => {
    link.addEventListener('click', () => {
      header.classList.remove('nav-open');
    });
  });
}

async function loadOffers(lang) {
  const offersContainer = document.getElementById('offersList');
  const emptyText = document.getElementById('offersEmpty');
  if (!offersContainer) return;

  try {
    const res = await fetch('/api/offers');
    const offers = await res.json();
    offersContainer.innerHTML = '';

    if (!offers.length) {
      emptyText.style.display = 'block';
      return;
    }
    emptyText.style.display = 'none';

    offers.forEach((offer) => {
      const card = document.createElement('article');
      card.className = 'card offer-card fade-in';

      const title = lang === 'ar' ? offer.titleAr : offer.titleEn;
      const desc = lang === 'ar' ? offer.descriptionAr : offer.descriptionEn;

      card.innerHTML = `
        ${offer.highlight ? '<span class="offer-pill">★ Highlight</span>' : ''}
        <h3>${title}</h3>
        ${desc ? `<p>${desc}</p>` : ''}
        ${offer.price ? `<p class="offer-price">${offer.price}</p>` : ''}
      `;

      offersContainer.appendChild(card);
      requestAnimationFrame(() => card.classList.add('visible'));
    });
  } catch (err) {
    console.error('Failed to load offers', err);
    emptyText.style.display = 'block';
  }
}

function initOffers() {
  const currentLang = localStorage.getItem('siteLang') || 'en';
  loadOffers(currentLang);

  document.querySelectorAll('.lang-btn').forEach((btn) => {
    btn.addEventListener('click', () => {
      const lang = btn.dataset.lang;
      loadOffers(lang);
    });
  });
}

async function loadPortfolio(lang) {
  const container = document.getElementById('portfolioList');
  const empty = document.getElementById('portfolioEmpty');
  if (!container || !empty) return;

  try {
    const res = await fetch('/api/portfolio');
    const items = await res.json();
    container.innerHTML = '';

    if (!items.length) {
      empty.style.display = 'block';
      return;
    }
    empty.style.display = 'none';

    items.forEach((item, index) => {
      const card = document.createElement('article');
      card.className = 'card portfolio-card fade-in';

      const thumbClass = index % 3 === 0 ? 'portfolio-thumb-1' : index % 3 === 1 ? 'portfolio-thumb-2' : 'portfolio-thumb-3';
      const title = lang === 'ar' ? item.titleAr : item.titleEn;
      const desc = lang === 'ar' ? item.descriptionAr : item.descriptionEn;

      card.innerHTML = `
        <div class="portfolio-thumb ${thumbClass}"></div>
        <div class="portfolio-body">
          <h3>${title}</h3>
          ${desc ? `<p>${desc}</p>` : ''}
          ${item.tag ? `<p class="offer-price">${item.tag}</p>` : ''}
        </div>
      `;

      container.appendChild(card);
      requestAnimationFrame(() => card.classList.add('visible'));
    });
  } catch (err) {
    console.error('Failed to load portfolio', err);
    empty.style.display = 'block';
  }
}

function initPortfolio() {
  const currentLang = localStorage.getItem('siteLang') || 'en';
  loadPortfolio(currentLang);

  document.querySelectorAll('.lang-btn').forEach((btn) => {
    btn.addEventListener('click', () => {
      const lang = btn.dataset.lang;
      loadPortfolio(lang);
    });
  });
}

function initForm() {
  const form = document.getElementById('requestForm');
  const statusEl = document.getElementById('formStatus');
  if (!form || !statusEl) return;

  form.addEventListener('submit', async (e) => {
    e.preventDefault();
    statusEl.textContent = '';
    statusEl.className = 'form-status';

    const data = {
      name: form.name.value.trim(),
      email: form.email.value.trim(),
      phone: form.phone.value.trim(),
      websiteType: form.websiteType.value,
      message: form.message.value.trim(),
    };

    if (!data.name || !data.email || !data.phone || !data.websiteType) {
      statusEl.textContent =
        (document.documentElement.lang === 'ar'
          ? 'يرجى تعبئة جميع الحقول المطلوبة.'
          : 'Please fill in all required fields.') || '';
      statusEl.classList.add('error');
      return;
    }

    try {
      const res = await fetch('https://formspree.io/f/mgonkrnl', {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify(data),
      });
      if (!res.ok) throw new Error('Request failed');
      form.reset();
      statusEl.textContent =
        document.documentElement.lang === 'ar'
          ? 'تم إرسال طلبك بنجاح. سنقوم بالتواصل معك قريبًا.'
          : 'Your request has been sent. We will contact you shortly.';
      statusEl.classList.add('success');
    } catch (err) {
      console.error(err);
      statusEl.textContent =
        document.documentElement.lang === 'ar'
          ? 'حدث خطأ أثناء إرسال الطلب. حاول مرة أخرى.'
          : 'Something went wrong. Please try again.';
      statusEl.classList.add('error');
    }
  });
}

async function initContact() {
  const emailLink = document.getElementById('contactEmailLink');
  const waLink = document.getElementById('contactWhatsAppLink');
  const instaLink = document.getElementById('contactInstagramLink');
  if (!emailLink || !waLink || !instaLink) return;

  try {
    const res = await fetch('/api/contact');
    const contact = await res.json();
    if (contact.email) {
      emailLink.href = `mailto:${contact.email}`;
      emailLink.textContent = contact.email;
    }
    if (contact.phone) {
      const digits = String(contact.phone).replace(/\D/g, '');
      if (digits) {
        waLink.href = `https://wa.me/${digits}`;
      }
    }
    if (contact.instagram) {
      instaLink.href = contact.instagram;
    }
  } catch (err) {
    console.error('Failed to load contact info', err);
  }
}

function initRevealAnimations() {
  const observer = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          entry.target.classList.add('visible');
          observer.unobserve(entry.target);
        }
      });
    },
    {
      threshold: 0.15,
    }
  );

  document.querySelectorAll('.card, .service-card, .portfolio-card').forEach((el) => {
    el.classList.add('fade-in');
    observer.observe(el);
  });
}

document.addEventListener('DOMContentLoaded', () => {
  const yearEl = document.getElementById('year');
  if (yearEl) yearEl.textContent = new Date().getFullYear();

  initLanguage();
  initSmoothScroll();
  initNavToggle();
  initOffers();
  initPortfolio();
  initForm();
  initContact();
  initRevealAnimations();
});

